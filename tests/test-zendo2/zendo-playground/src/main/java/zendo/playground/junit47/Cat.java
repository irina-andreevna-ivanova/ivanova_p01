package zendo.playground.junit47;

/**
 * @author mocanu
 */
public class Cat {

    private String name;

    /**
     * @param name
     */
    public Cat(String name) {
        super();
        this.name = name;
    }

    /**
     * Returns the name
     * 
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name to the given value.
     * 
     * @param name the name to set
     */
    public void setName( String name ) {
        this.name = name;
    }

}
